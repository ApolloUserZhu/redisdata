    private static final ComplexEntity COMPLEX_ENTITY_1 = new ComplexEntity("Value", 999, 999l, 1.0, 1.0f, 'a');
    private static final ComplexEntity COMPLEX_ENTITY_2 = new ComplexEntity("Value2", 1000, 1000l, 2.0, 2.0f, 'b');

    @Autowired
    private ApplicationContext applicationContext;

    @Autowired
    private JacksonJsonDataMapper<ComplexEntity> jacksonJsonDataMapper;

    private String currentListComplexEntityCommandServiceName;
    private ListCommand<Integer, ComplexEntity> currentListComplexEntityCommandService;

    public ListCommandIntegrationTest(String currentListComplexEntityCommandServiceName) {
        this.currentListComplexEntityCommandServiceName = currentListComplexEntityCommandServiceName;
    }

    @Parameterized.Parameters
    public static List<Object[]> data() {
        return Arrays.asList(new Object[][]{
                {"listCommandComplexEntityService"},
                {"listCommandComplexEntityPipelinedService"},
                {"listCommandComplexEntityTransactionalService"},
                {"listCommandComplexEntityPipelinedTransactionalService"}
        });
    }

    @Before
    public void setUp() throws Exception {
        TestContextManager testContextManager = new TestContextManager(this.getClass());
        testContextManager.prepareTestInstance(this);

        currentListComplexEntityCommandService = (ListCommand) applicationContext.getBean(currentListComplexEntityCommandServiceName);
    }

    @Test
    public void testBlPopExistingKey() throws Exception {

        primaryJedis.rpush("999".getBytes(Charsets.UTF_8), jacksonJsonDataMapper.serialize(COMPLEX_ENTITY_1));
        final Response<LPair<Integer, ComplexEntity>> response = currentListComplexEntityCommandService.blPop(999, 1000);

        assertThat(response.get().getKey(), is(999));
        assertThat(response.get().getValue(), is(COMPLEX_ENTITY_1));
    }

    @Test
    public void testBlPopWithExpiresExistingKey() throws Exception {

        primaryJedis.rpush("999".getBytes(Charsets.UTF_8), jacksonJsonDataMapper.serialize(COMPLEX_ENTITY_1));
        final Response<LPair<Integer, ComplexEntity>> response = currentListComplexEntityCommandService.blPop(new Integer[]{999, 1000}, 5);

        assertThat(response.get().getKey(), is(999));
        assertThat(response.get().getValue(), is(COMPLEX_ENTITY_1));
    }

    @Test
    public void testBlPopWithExpiresNonExistingKey() throws Exception {

        final Response<LPair<Integer, ComplexEntity>> response = currentListComplexEntityCommandService.blPop(new Integer[]{999, 1000}, 1);

        assertThat(response.get(), nullValue());
    }

