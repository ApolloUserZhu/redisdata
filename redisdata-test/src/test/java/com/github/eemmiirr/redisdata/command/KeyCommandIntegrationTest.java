package com.github.eemmiirr.redisdata.command;

import com.google.common.base.Charsets;
import com.github.eemmiirr.redisdata.AbstractRedisTest;
import com.github.eemmiirr.redisdata.testinfrastructure.entity.ComplexEntity;
import com.github.eemmiirr.redisdata.response.Encoding;
import com.github.eemmiirr.redisdata.response.Response;
import com.github.eemmiirr.redisdata.response.Status;
import com.github.eemmiirr.redisdata.response.Type;
import org.junit.*;
import org.junit.runner.RunWith;
import org.junit.runners.Parameterized;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.ApplicationContext;
import org.springframework.test.context.ContextConfiguration;
import org.springframework.test.context.TestContextManager;

import java.util.Arrays;
import java.util.List;
import java.util.Set;

import static org.hamcrest.MatcherAssert.assertThat;
import static org.hamcrest.Matchers.*;

/**
 * @author Emir Dizdarevic
 * @since 0.7
 */
@RunWith(Parameterized.class)
@ContextConfiguration("classpath:integrationTestContext.xml")
public class KeyCommandIntegrationTest extends AbstractRedisTest {

    private static final ComplexEntity COMPLEX_ENTITY_1 = new ComplexEntity("Value", 999, 999l, 1.0, 1.0f, 'a');
    private static final ComplexEntity COMPLEX_ENTITY_2 = new ComplexEntity("Value2", 1000, 1000l, 2.0, 2.0f, 'b');

    @Autowired
    private ApplicationContext applicationContext;

    private String currentKeyCommandComplexEntityServiceName;
    private KeyCommand<Integer, ComplexEntity> currentKeyCommandComplexEntityService;

    private String currentKeyCommandIntegerServiceName;
    private KeyCommand<Integer, Integer> currentKeyCommandIntegerService;

    public KeyCommandIntegrationTest(String currentKeyCommandComplexEntityServiceName, String currentKeyCommandIntegerServiceName) {
        this.currentKeyCommandComplexEntityServiceName = currentKeyCommandComplexEntityServiceName;
        this.currentKeyCommandIntegerServiceName = currentKeyCommandIntegerServiceName;
    }

    @Parameterized.Parameters
    public static List<Object[]> data() {
        return Arrays.asList(new Object[][]{
                {"keyCommandComplexEntityService", "keyCommandIntegerService"},
                {"keyCommandComplexEntityPipelinedService", "keyCommandIntegerPipelinedService"},
                {"keyCommandComplexEntityTransactionalService", "keyCommandIntegerTransactionalService"},
                {"keyCommandComplexEntityPipelinedTransactionalService", "keyCommandIntegerPipelinedTransactionalService"}
        });
    }

    @Before
    public void setUp() throws Exception {
        TestContextManager testContextManager = new TestContextManager(this.getClass());
        testContextManager.prepareTestInstance(this);

        currentKeyCommandComplexEntityService = (KeyCommand) applicationContext.getBean(currentKeyCommandComplexEntityServiceName);
        currentKeyCommandIntegerService = (KeyCommand) applicationContext.getBean(currentKeyCommandIntegerServiceName);
    }

    @Test
    public void testDel() throws Exception {

        primaryJedis.set("1", COMPLEX_ENTITY_1.toString());
        final Response<Boolean> positivResult = currentKeyCommandComplexEntityService.del(1);
        final Response<Boolean> negativResult = currentKeyCommandComplexEntityService.del(2);

        assertThat(positivResult.get(), is(true));
        assertThat(negativResult.get(), is(false));
    }

    @Test
    public void testDump() throws Exception {

        primaryJedis.set("1", "12345");
        final Response<byte[]> positivResult = currentKeyCommandIntegerService.dump(1);

        assertThat(positivResult.get(), is(new byte[]{0, -63, 57, 48, 6, 0, 11, 121, -126, -89, 102, -87, 52, 42}));
    }

    @Test
    public void testExist() throws Exception {

        primaryJedis.set("1", COMPLEX_ENTITY_1.toString());
        final Response<Boolean> positivResult = currentKeyCommandComplexEntityService.exists(1);
        final Response<Boolean> negativResult = currentKeyCommandComplexEntityService.exists(2);

        assertThat(positivResult.get(), is(true));
        assertThat(negativResult.get(), is(false));
    }

    @Test
    public void testExpire() throws Exception {

        primaryJedis.set("1", COMPLEX_ENTITY_1.toString());
        final Response<Boolean> positivResult = currentKeyCommandComplexEntityService.expire(1, 1000);
        final Response<Boolean> negativResult = currentKeyCommandComplexEntityService.expire(2, 1000);
        final Long ttl = primaryJedis.ttl("1");

        assertThat(positivResult.get(), is(true));
        assertThat(negativResult.get(), is(false));
        assertThat(ttl, allOf(greaterThan(1000l - 2), lessThanOrEqualTo(1000l)));
    }

    @Test
    public void testExpireAt() throws Exception {

        primaryJedis.set("1", COMPLEX_ENTITY_1.toString());
        final Response<Boolean> positivResult = currentKeyCommandComplexEntityService.expireAt(1, 1893452400l);  // 2030-01-01
        final Response<Boolean> negativResult = currentKeyCommandComplexEntityService.expireAt(2, 1893452400l);
        final Long ttl = primaryJedis.ttl("1");
        final Long calculatedTtl = 1893452400l - System.currentTimeMillis() / 1000;

        assertThat(positivResult.get(), is(true));
        assertThat(negativResult.get(), is(false));
        assertThat(ttl, allOf(greaterThan(calculatedTtl - 2), lessThanOrEqualTo(calculatedTtl)));
    }

    @Test
    public void testKeys() throws Exception {

        primaryJedis.set("123", COMPLEX_ENTITY_1.toString());
        primaryJedis.set("1222", COMPLEX_ENTITY_2.toString());
        primaryJedis.set("1332", COMPLEX_ENTITY_1.toString());
        final Response<Set<String>> results = currentKeyCommandComplexEntityService.keys("*2*", Charsets.UTF_8);

        assertThat(results.get(), hasSize(3));
        assertThat(results.get(), containsInAnyOrder("123", "1222", "1332"));
    }

    @Test
    public void testMigrate() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());

        final String migratedValueBeforeMigration = secondaryJedis.get("999");
        final Response<Status> results = currentKeyCommandComplexEntityService.migrate(999, "localhost", 6380, 0, 1);
        final String migratedValueAfterMigration = secondaryJedis.get("999");
        final String originalValueAfterMigration = primaryJedis.get("999");

        assertThat(results.get().getType(), is(Status.Type.OK));
        assertThat(migratedValueBeforeMigration, nullValue());
        assertThat(originalValueAfterMigration, nullValue());
        assertThat(migratedValueAfterMigration, is(COMPLEX_ENTITY_1.toString()));
    }

    @Test
    public void testMove() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());

        primaryJedis.select(1);
        final String migratedValueBeforeMigration = primaryJedis.get("999");
        final Response<Boolean> result = currentKeyCommandComplexEntityService.move(999, 1);
        final String migratedValueAfterMigration = primaryJedis.get("999");
        primaryJedis.select(0);
        final String originalValueAfterMigration = primaryJedis.get("999");

        assertThat(result.get(), is(true));
        assertThat(migratedValueBeforeMigration, nullValue());
        assertThat(originalValueAfterMigration, nullValue());
        assertThat(migratedValueAfterMigration, is(COMPLEX_ENTITY_1.toString()));
    }

    @Test
    public void testObjectEncoding() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        final Response<Encoding> encoding = currentKeyCommandComplexEntityService.objectEncoding(999);

        assertThat(encoding.get(), is(Encoding.RAW));
    }

    @Test
    public void testObjectRefCount() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        final Response<Long> refCount = currentKeyCommandComplexEntityService.objectRefCount(999);

        assertThat(refCount.get(), is(1l));
    }

    @Test
    public void testObjectIdleTime() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        final Response<Long> idleTime = currentKeyCommandComplexEntityService.objectIdleTime(999);

        assertThat(idleTime.get(), is(0l));
    }

    @Test
    public void testPersist() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        primaryJedis.expire("999", 1000);
        final Response<Boolean> response = currentKeyCommandComplexEntityService.persist(999);
        final long expires = primaryJedis.ttl("999");

        assertThat(response.get(), is(true));
        assertThat(expires, is(-1l));
    }

    @Test
    public void testPexpire() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        final Response<Boolean> response = currentKeyCommandComplexEntityService.pexpire(999, 1000);
        final long expires = primaryJedis.ttl("999");

        assertThat(response.get(), is(true));
        assertThat(expires, allOf(greaterThan(0l), lessThan(1000l)));
    }

    @Test
    public void testPexpireAt() throws Exception {

        final long nowMillis = System.currentTimeMillis();
        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        final Response<Boolean> response = currentKeyCommandComplexEntityService.pexpireAt(999, nowMillis + 1000);
        final long expires = primaryJedis.ttl("999");

        assertThat(response.get(), is(true));
        assertThat(expires, allOf(greaterThan(0l), lessThan(1000l)));
    }

    @Test
    public void testPttl() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        primaryJedis.pexpire("999", 1000);
        final Response<Long> response = currentKeyCommandComplexEntityService.pttl(999);

        assertThat(response.get(), allOf(greaterThan(0l), lessThanOrEqualTo(1000l)));
    }

    @Test
    public void testRandomKeyExistent() throws Exception {

        primaryJedis.set("999", "1");
        final Response<Integer> response = currentKeyCommandIntegerService.randomKey();
        assertThat(response.get(), is(999));
    }

    @Test
    public void testRandomKeyNonExistent() throws Exception {

        final Response<Integer> response = currentKeyCommandIntegerService.randomKey();
        assertThat(response.get(), nullValue());
    }

    @Test
    public void testRename() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        final Response<Status> response = currentKeyCommandComplexEntityService.rename(999, 1000);
        final String value = primaryJedis.get("1000");

        assertThat(response.get().getType(), is(Status.Type.OK));
        assertThat(value, is(COMPLEX_ENTITY_1.toString()));
    }

    @Test
    public void testRenameNx() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        final Response<Boolean> response = currentKeyCommandComplexEntityService.renameNx(999, 1000);
        final String value = primaryJedis.get("1000");

        assertThat(response.get(), is(true));
        assertThat(value, is(COMPLEX_ENTITY_1.toString()));
    }

    @Test
    public void testRestore() throws Exception {

        final byte[] dumpedData = new byte[]{0, -63, 57, 48, 6, 0, 11, 121, -126, -89, 102, -87, 52, 42};
        final Response<Status> response = currentKeyCommandComplexEntityService.restore(1000, dumpedData, 1000);
        final String value = primaryJedis.get("1000");

        assertThat(response.get().getType(), is(Status.Type.OK));
        assertThat(value, is("12345"));
    }

    @Test
    public void testSort() throws Exception {

        primaryJedis.rpush("999", "10");
        primaryJedis.rpush("999", "5");
        primaryJedis.rpush("999", "1");
        final Response<List<Integer>> response = currentKeyCommandIntegerService.sort(999);

        assertThat(response.get(), hasSize(3));
        assertThat(response.get().get(0), is(1));
        assertThat(response.get().get(1), is(5));
        assertThat(response.get().get(2), is(10));
    }

    @Test
    public void testSortWithDestKey() throws Exception {

        primaryJedis.rpush("999", "10");
        primaryJedis.rpush("999", "5");
        primaryJedis.rpush("999", "1");
        final Response<Long> response = currentKeyCommandIntegerService.sort(999, 1000);
        final boolean srcKeyExistence = primaryJedis.exists("999");
        final boolean destKeyExistence = primaryJedis.exists("1000");

        assertThat(response.get(), is(3l));
        assertThat(srcKeyExistence, is(true));
        assertThat(destKeyExistence, is(true));
    }

    @Test
    public void testTtl() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        final Response<Long> response = currentKeyCommandComplexEntityService.ttl(999);

        assertThat(response.get(), is(-1l));
    }

    @Test
    public void testType() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        final Response<Type> response = currentKeyCommandComplexEntityService.type(999);

        assertThat(response.get(), is(Type.STRING));
    }

    @Test
    public void testLockSuccess() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        final Response<Boolean> response = currentKeyCommandComplexEntityService.lock(999);
        final String lockValue = primaryJedis.get("lock.999");

        assertThat(response.get(), is(true));
        assertThat(lockValue, is("1"));
    }

    @Test
    public void testLockFail() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        primaryJedis.set("lock.999", "1");
        final Response<Boolean> response = currentKeyCommandComplexEntityService.lock(999);
        final String lockValue = primaryJedis.get("lock.999");

        assertThat(response.get(), is(false));
        assertThat(lockValue, is("1"));
    }

    @Test
    public void testUnlockFail() throws Exception {

        primaryJedis.set("999", COMPLEX_ENTITY_1.toString());
        final Response<Boolean> response = currentKeyCommandComplexEntityService.unlock(999);
        final String lockValue = primaryJedis.get("lock.999");

        assertThat(response.get(), is(false));
        assertThat(lockValue, nullValue());
    }

    @Test
    @Ignore("Available from 2.8")
    public void testScan() throws Exception {
    }
}
