    private String currentStringIntegerCommandServiceName;
    private StringCommand<Integer, Integer> currentStringIntegerCommandService;

    public StringCommandIntegrationTest(String currentStringComplexEntityCommandServiceName, String currentStringIntegerCommandServiceName) {
        this.currentStringComplexEntityCommandServiceName = currentStringComplexEntityCommandServiceName;
        this.currentStringIntegerCommandServiceName = currentStringIntegerCommandServiceName;
    }

    @Parameterized.Parameters
    public static List<Object[]> data() {
        return Arrays.asList(new Object[][]{
                {"stringCommandComplexEntityService", "stringCommandIntegerService"},
                {"stringCommandComplexEntityPipelinedService", "stringCommandIntegerPipelinedService"},
                {"stringCommandComplexEntityTransactionalService", "stringCommandIntegerTransactionalService"},
                {"stringCommandComplexEntityPipelinedTransactionalService", "stringCommandIntegerPipelinedTransactionalService"}
        });
    }

    @Before
    public void setUp() throws Exception {
        TestContextManager testContextManager = new TestContextManager(this.getClass());
        testContextManager.prepareTestInstance(this);

        currentStringComplexEntityCommandService = (StringCommand) applicationContext.getBean(currentStringComplexEntityCommandServiceName);
        currentStringIntegerCommandService = (StringCommand) applicationContext.getBean(currentStringIntegerCommandServiceName);
    }

    @Test
    public void testAppendExistingKey() throws Exception {

        primaryJedis.set("999", "12345");
        final Response<Long> response = currentStringIntegerCommandService.append(999, 6789);
        final String valueAfterAppend = primaryJedis.get("999");

        assertThat(response.get(), is(9l));
        assertThat(valueAfterAppend, is("123456789"));
    }

    @Test
    public void testAppendNonExistingKey() throws Exception {

        final Response<Long> response = currentStringIntegerCommandService.append(999, 12345);
        final String valueAfterAppend = primaryJedis.get("999");

        assertThat(response.get(), is(5l));
        assertThat(valueAfterAppend, is("12345"));
    }

