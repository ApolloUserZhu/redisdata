    @Test
    public void testBitOpExistingKey() throws Exception {

        primaryJedis.set("999".getBytes(Charsets.UTF_8), new byte[]{-1, -1, -1, -1});
        primaryJedis.set("1000".getBytes(Charsets.UTF_8), new byte[]{-1, -1, -1, -1});
        final Response<Long> response = currentStringIntegerCommandService.bitOp(BinaryOperation.XOR, 1001, 1000, 999);
        final byte[] afterBitOpValue = primaryJedis.get("1001".getBytes(Charsets.UTF_8));

        assertThat(response.get(), is(4l));
        assertThat(afterBitOpValue, is(new byte[]{0, 0, 0, 0}));
    }

