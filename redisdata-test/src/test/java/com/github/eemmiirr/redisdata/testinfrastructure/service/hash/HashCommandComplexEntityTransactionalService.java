public class HashCommandComplexEntityTransactionalService implements HashCommand<Integer, ComplexEntity> {

    @Autowired
    @Qualifier("hashCommandComplexEntityBinding")
    private HashCommand<Integer, ComplexEntity> hashCommandComplexEntityBinding;

    @Override
    public Response<Long> hDel(@Nonnull Integer key, @Nonnull Integer... fields) {
        return hashCommandComplexEntityBinding.hDel(key, fields);
    }

    @Override
    public Response<Boolean> hExists(@Nonnull Integer key, @Nonnull Integer field) {
        return hashCommandComplexEntityBinding.hExists(key, field);
    }

    @Override
    public Response<ComplexEntity> hGet(@Nonnull Integer key, @Nonnull Integer field) {
        return hashCommandComplexEntityBinding.hGet(key, field);
    }

    @Override
    public Response<Map<Integer, ComplexEntity>> hGetAll(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.hGetAll(key);
    }

    @Override
    public Response<Long> hIncrBy(@Nonnull Integer key, @Nonnull Integer field, long increment) {
        return hashCommandComplexEntityBinding.hIncrBy(key, field, increment);
    }

    @Override
    public Response<Double> hIncrByFloat(@Nonnull Integer key, @Nonnull Integer field, double increment) {
        return hashCommandComplexEntityBinding.hIncrByFloat(key, field, increment);
    }

    @Override
    public Response<Set<Integer>> hKeys(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.hKeys(key);
    }

    @Override
    public Response<Long> hLen(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.hLen(key);
    }

    @Override
    public Response<List<ComplexEntity>> hMGet(@Nonnull Integer key, @Nonnull Integer... fields) {
        return hashCommandComplexEntityBinding.hMGet(key, fields);
    }

    @Override
    public Response<Status> hMSet(@Nonnull Integer key, @Nonnull Map<Integer, ComplexEntity> keyValueMap) {
        return hashCommandComplexEntityBinding.hMSet(key, keyValueMap);
    }

    @Override
    public Response<Boolean> hSet(@Nonnull Integer key, @Nonnull Integer field, @Nonnull ComplexEntity value) {
        return hashCommandComplexEntityBinding.hSet(key, field, value);
    }

    @Override
    public Response<Boolean> hSetNx(@Nonnull Integer key, @Nonnull Integer field, @Nonnull ComplexEntity value) {
        return hashCommandComplexEntityBinding.hSetNx(key, field, value);
    }

    @Override
    public Response<List<ComplexEntity>> hVals(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.hVals(key);
    }

    @Override
    public Response<Boolean> del(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.del(key);
    }

    @Override
    public Response<byte[]> dump(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.dump(key);
    }

    @Override
    public Response<Boolean> exists(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.exists(key);
    }

    @Override
    public Response<Boolean> expire(@Nonnull Integer key, int seconds) {
        return hashCommandComplexEntityBinding.expire(key, seconds);
    }

    @Override
    public Response<Boolean> expireAt(@Nonnull Integer key, long unixTimestampSeconds) {
        return hashCommandComplexEntityBinding.expireAt(key, unixTimestampSeconds);
    }

    @Override
    public Response<Set<String>> keys(@Nonnull String pattern, @Nonnull Charset encoding) {
        return hashCommandComplexEntityBinding.keys(pattern, encoding);
    }

    @Override
    public Response<Status> migrate(@Nonnull Integer key, @Nonnull String host, int port, int destinationDb, int timeoutInSeconds) {
        return hashCommandComplexEntityBinding.migrate(key, host, port, destinationDb, timeoutInSeconds);
    }

    @Override
    public Response<Boolean> move(@Nonnull Integer key, int dbIndex) {
        return hashCommandComplexEntityBinding.move(key, dbIndex);
    }

    @Override
    public Response<Long> objectRefCount(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.objectRefCount(key);
    }

    @Override
    public Response<Encoding> objectEncoding(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.objectEncoding(key);
    }

    @Override
    public Response<Long> objectIdleTime(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.objectIdleTime(key);
    }

    @Override
    public Response<Boolean> persist(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.persist(key);
    }

    @Override
    public Response<Boolean> pexpire(@Nonnull Integer key, int millis) {
        return hashCommandComplexEntityBinding.pexpire(key, millis);
    }

    @Override
    public Response<Boolean> pexpireAt(@Nonnull Integer key, long unixTimestampMillis) {
        return hashCommandComplexEntityBinding.pexpireAt(key, unixTimestampMillis);
    }

    @Override
    public Response<Long> pttl(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.pttl(key);
    }

    @Override
    public Response<Integer> randomKey() {
        return hashCommandComplexEntityBinding.randomKey();
    }

    @Override
    public Response<Status> rename(@Nonnull Integer oldKey, @Nonnull Integer newKey) {
        return hashCommandComplexEntityBinding.rename(oldKey, newKey);
    }

    @Override
    public Response<Boolean> renameNx(@Nonnull Integer oldKey, @Nonnull Integer newKey) {
        return hashCommandComplexEntityBinding.renameNx(oldKey, newKey);
    }

    @Override
    public Response<Status> restore(@Nonnull Integer key, byte[] data, int ttl) {
        return hashCommandComplexEntityBinding.restore(key, data, ttl);
    }

    @Override
    public Response<List<ComplexEntity>> sort(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.sort(key);
    }

    @Override
    public Response<Long> sort(@Nonnull Integer srcKey, @Nonnull Integer destKey) {
        return hashCommandComplexEntityBinding.sort(srcKey, destKey);
    }

    @Override
    public Response<Long> ttl(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.ttl(key);
    }

    @Override
    public Response<Type> type(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.type(key);
    }

    @Override
    public Response<Boolean> lock(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.lock(key);
    }

    @Override
    public Response<Boolean> unlock(@Nonnull Integer key) {
        return hashCommandComplexEntityBinding.unlock(key);
    }
}
