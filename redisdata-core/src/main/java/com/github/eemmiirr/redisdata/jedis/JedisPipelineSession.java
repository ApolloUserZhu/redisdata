    private <T> redis.clients.jedis.Response<T> invokeUnresolvable(Unresolvable unresolvable, Object... args) {
        try {
            final Method method;
            if (pipelineOrTransaction instanceof Pipeline) {
                method = pipelineMethodMap.get(unresolvable.getName());
            } else {
                method = transactionMethodMap.get(unresolvable.getName());
            }
            return (redis.clients.jedis.Response<T>) method.invoke(pipelineOrTransaction, args);
        } catch (IllegalAccessException e) {

            // This is impossible to happen
            UncheckedThrow.throwUnchecked(e);
        } catch (InvocationTargetException e) {

            // This is impossible to happen
            UncheckedThrow.throwUnchecked(e);
        }

        // To satisfy the compiler
        return null;
    }

