    private static final String UNSUPPORTED_OPERATION_MESSAGE = "Operation not supported in Pipeline/Transaction";

    private enum Unresolvable {
        DUMP("dump", byte[].class),
        MIGRATE("migrate", byte[].class, int.class, byte[].class, int.class, int.class),
        OBJECT_REF_COUNT("objectRefcount", byte[].class),
        OBJECT_ENCODING("objectEncoding", byte[].class),
        OBJECT_IDLE_TIME("objectIdletime", byte[].class),
        PEXPIRE("pexpire", byte[].class, int.class),
        PEXPIRE_AT("pexpireAt", byte[].class, long.class),
        PTTL("pttl", byte[].class),
        RESTORE("restore", byte[].class, int.class, byte[].class),
        INCR_BY_FLOAT("incrByFloat", byte[].class, double.class),
        PSET_EX("psetex", byte[].class, int.class, byte[].class),
        S_RAND_MEMBER("srandmember", byte[].class, int.class),
        H_INCR_BY_FLOAT("hincrByFloat", byte[].class, byte[].class, double.class);

        private final String name;
        private final Class[] argTypes;

        private Unresolvable(String name, Class... argTypes) {
            this.name = name;
            this.argTypes = argTypes;
        }

        private String getName() {
            return name;
        }

        private Class[] getArgTypes() {
            return argTypes;
        }
    }

    private static final Map<String, Method> pipelineMethodMap = new HashMap<String, Method>();
    private static final Map<String, Method> transactionMethodMap = new HashMap<String, Method>();

    static {
        for (Unresolvable unresolvable : Unresolvable.values()) {
            try {
                if (!pipelineMethodMap.containsKey(unresolvable.getName())) {
                    pipelineMethodMap.put(unresolvable.getName(), Pipeline.class.getMethod(unresolvable.getName(), unresolvable.getArgTypes()));
                }
                if (!transactionMethodMap.containsKey(unresolvable.getName())) {
                    transactionMethodMap.put(unresolvable.getName(), Transaction.class.getMethod(unresolvable.getName(), unresolvable.getArgTypes()));
                }
            } catch (Exception e) {
                throw new SessionInitializationExcpetion("Redis data could not be initialized. Unable to find unresolvable " + unresolvable, e);
            }
        }
    }

    private final BinaryRedisPipeline binaryRedisPipeline;
    private final MultiKeyBinaryRedisPipeline multiKeyBinaryRedisPipeline;
    private final Object pipelineOrTransaction;

